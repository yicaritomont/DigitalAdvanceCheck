<h2>Reglamento General de Protección de Datos (RGPD) de la Unión Europea</h2>
<p>
    <strong>Alcance:</strong> El RGPD, en vigor desde mayo de 2018, se aplica a todas las empresas y organizaciones que procesan datos personales de residentes de la Unión Europea, independientemente de dónde se encuentren ubicadas.
</p>
<p>
    <strong>Principios clave:</strong> Protección por diseño y por defecto, consentimiento explícito, derecho al olvido, portabilidad de datos, y notificación de violaciones de seguridad.
</p>
<p>
    <strong>Transferencias internacionales:</strong> Establece condiciones para la transferencia de datos personales fuera de la UE, asegurando que el país receptor ofrezca un nivel de protección adecuado o mediante mecanismos como cláusulas contractuales tipo.
</p>

<h2>En Colombia</h2>
<p>La protección de datos personales está regulada por varias normativas que establecen los principios, derechos, obligaciones y procedimientos para el manejo adecuado de la información personal. A continuación, se detallan las principales leyes y regulaciones colombianas en este ámbito:</p>

<h3>Ley 1581 de 2012</h3>
<p>
    <strong>Alcance:</strong> Conocida como la Ley General de Protección de Datos, establece el marco general para la protección de datos personales en Colombia. Es aplicable a todas las personas naturales o jurídicas, públicas o privadas, que traten datos personales en el país.
</p>
<p>
    <strong>Principios:</strong> Finalidad, libertad, veracidad o calidad, transparencia, acceso y circulación restringida, seguridad, y confidencialidad.
</p>
<p>
    <strong>Derechos de los titulares:</strong> Conocer, actualizar y rectificar sus datos personales; ser informado sobre el uso de sus datos; revocar la autorización para el tratamiento de sus datos.
</p>

<h3>Decreto 1377 de 2013</h3>
<p>
    <strong>Propósito:</strong> Reglamenta algunos aspectos de la Ley 1581, especialmente en lo relacionado con las políticas de tratamiento de datos personales y la forma de solicitar autorización a los titulares para el tratamiento de sus datos.
</p>
<p>
    <strong>Implicaciones:</strong> Obliga a las empresas a adoptar medidas para garantizar el respeto a los derechos de los titulares de los datos y a implementar políticas claras y públicas sobre el tratamiento de los datos.
</p>

<h3>Decreto 1074 de 2015</h3>
<p>
    <strong>Alcance:</strong> Este decreto compila y reestructura todas las normas del sector comercio, industria y turismo, incluyendo las relacionadas con la protección de datos personales.
</p>
<p>
    <strong>Estructura:</strong> Incluye disposiciones sobre cómo se deben manejar los datos personales en el sector empresarial y turístico, y las responsabilidades de las empresas para proteger estos datos.
</p>

<h3>Decreto 886 de 2014</h3>
<p>
    <strong>Descripción:</strong> Reglamenta el registro nacional de bases de datos. Las empresas que manejan bases de datos deberán inscribirse en este registro, administrado por la Superintendencia de Industria y Comercio (SIC).
</p>
<p>
    <strong>Obligación:</strong> Todas las empresas que operen en Colombia y manejen datos personales deben registrar sus bases de datos, indicando el tipo de datos recopilados y el uso que se les dará.
</p>
